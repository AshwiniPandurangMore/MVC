package mypack;

public class Book
{
	int bookid,price;
	String title;
	
	public int getBookid()
	{
		return bookid;
	}
	
	public void setBookid(int bookid) 	
	{
		this.bookid = bookid;
	}
	
	public int getPrice()
	{
		return price;
	}
	
	public void setPrice(int price)
	{
		this.price = price;
	}
	
	public String getTitle()
	{
		return title;
	}
	
	public void setTitle(String title)
	{
		this.title = title;
	}
}